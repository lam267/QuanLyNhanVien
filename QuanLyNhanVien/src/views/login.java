     void login(){
         userDAO dao = new userDAO();
         List<user> list = dao.selectUser(txtUserName.getText());
         if (list.size()==1){
             if (list.get(0).getPassword().equals(txtPassWord.getText())){
                Home home =new Home();
                this.disable();
                home.setVisible(true);
                
             }else
                 JOptionPane.showMessageDialog(this, "Sai mật khẩu !");
         }else{
             JOptionPane.showMessageDialog(this, "Username không tồn tại!");
         }
     }

